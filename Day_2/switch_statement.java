public class switch_statement {
    public static void main(String[] args) {
        int a = 100;
        switch (a) {
            default:
                System.out.println("invalid case");
                break;
            case 5:
                System.out.println("five");
                break;
            case 10:
                System.out.println("ten");
                break;

        }
    }
}